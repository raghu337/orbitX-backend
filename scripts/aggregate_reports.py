def read_json(path, default=None):
    if not os.path.exists(path):
        return default if default is not None else {}
    try:
        with open(path, encoding="utf-8") as f:
            return json.load(f)
    except Exception as e:
        print(f"[Aggregator] Warning: Could not parse JSON at {path}: {e}")
        return default if default is not None else {}

def parse_junit(path):
    """Parses a JUnit XML file and returns test counts."""
    if not os.path.exists(path):
        return None
    try:
        root = ET.parse(path).getroot()
        suites = root.findall("testsuite") if root.tag == "testsuites" else [root]
        total = failed = skipped = errors = 0
        for s in suites:
            total   += int(s.attrib.get("tests",    0))
            failed  += int(s.attrib.get("failures", 0))
            errors  += int(s.attrib.get("errors",   0))
            skipped += int(s.attrib.get("skipped",  0))
        passed = total - failed - errors - skipped
        return {
            "total": total,
            "passed": max(0, passed),
            "failed": failed + errors,
            "skipped": skipped
        }
    except Exception as e:
        print(f"[Aggregator] Warning: Could not parse JUnit XML at {path}: {e}")
        return None

def mock_report(total, passed=None, failed=0, skipped=0):
    p = passed if passed is not None else total - failed
    return {"total": total, "passed": p, "failed": failed, "skipped": skipped}

