def gen_dashboard(data: dict, out="reports/dashboard.html"):
    comps = data.get("components", [])
    tot_t = sum(c["total"]  for c in comps)
    tot_p = sum(c["passed"] for c in comps)
    tot_f = sum(c["failed"] for c in comps)
    rate  = round(tot_p / tot_t * 100, 1) if tot_t else 0

    rows = [[c["name"], c["total"], c["passed"], c["failed"],
             f"{c.get('rate',0)}%", c.get("status","🟢 PASS")] for c in comps]
    rows.append(["✅ OVERALL", tot_t, tot_p, tot_f, f"{rate}%",
                 "🟢 PASS" if tot_f == 0 else "🔴 FAIL"])

    body = (
        _stats(("Total Cases", tot_t), ("Passed", tot_p),
               ("Failed", tot_f), ("Pass Rate", f"{rate}%"))
        + _table(["Component","Total","Passed","Failed","Pass %","Status"], rows)
    )
    _write(out, _page("OrbitX Verification Dashboard",
                      body, "Complete verification report for all modules"))


def gen_performance(data: dict, out="reports/performance.html"):
    g     = data.get("global", {})
    tiers = data.get("tiers",  {})
    rows  = []
    for key in ["100vu","300vu","500vu","1000vu"]:
        t = tiers.get(key, {})
        ok = t.get("errRate",0) < t.get("errLimit",5) and t.get("p95",0) < t.get("p95Limit",3000)
        rows.append([t.get("label",key), t.get("rps",0), t.get("avg",0), t.get("med",0),
                     t.get("p95",0), t.get("p99",0), f"{t.get('errRate',0)}%",
                     "🟢 PASS" if ok else "🔴 FAIL"])

    body = (
        _stats(("Total Requests", f"{g.get('reqs',0):,}"),
               ("Requests/sec",   g.get("rps",0)),
               ("Average (ms)",   g.get("avg",0)),
               ("P95 (ms)",       g.get("p95",0)),
               ("P99 (ms)",       g.get("p99",0)),
               ("Error Rate",     f"{g.get('errRate',0)}%"))
        + _table(["Tier","Req/s","Avg (ms)","Median","P95","P99","Errors","Status"], rows)
    )
    _write(out, _page("Performance Dashboard", body, "4-Tier Load Test Results"))


