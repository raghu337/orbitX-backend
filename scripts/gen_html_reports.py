def _stats(*pairs):
    html = '<div class="stat-grid">'
    for lbl, val in pairs:
        html += f'<div class="stat"><div class="stat-val">{val}</div><div class="stat-lbl">{lbl}</div></div>'
    return html + "</div>"


def _table(headers, rows):
    ths = "".join(f"<th>{h}</th>" for h in headers)
    trs = ""
    for row in rows:
        tds = ""
        for cell in row:
            cs = str(cell)
            if "🟢" in cs or "PASS" in cs:
                cls = "pass"
            elif "🔴" in cs or "FAIL" in cs:
                cls = "fail"
            elif "🟡" in cs or "WARN" in cs:
                cls = "warn"
            else:
                cls = ""
            tds += f'<td class="{cls}">{cs}</td>'
        trs += f"<tr>{tds}</tr>"
    return f"<table><thead><tr>{ths}</tr></thead><tbody>{trs}</tbody></table>"


def _bar(pct, label=""):
    color = "#10b981" if pct >= 80 else ("#f59e0b" if pct >= 60 else "#ef4444")
    return (
        f'<div style="display:flex;align-items:center;gap:12px;margin:8px 0;">'
        f'<span style="min-width:160px;font-size:.85rem;">{label}</span>'
        f'<div class="bar-wrap" style="flex:1"><div class="bar-fill" style="width:{pct}%;background:{color}"></div></div>'
        f'<span style="min-width:50px;text-align:right;font-size:.85rem;font-weight:600;">{pct}%</span>'
        f'</div>'
    )


