def gen_coverage(backend_pct: float, frontend_pct: float,
                 out="reports/coverage.html"):
    overall = round((backend_pct + frontend_pct) / 2, 1)
    mods = [
        ("app/api/auth",       92.5), ("app/api/satellites",  88.0),
        ("app/api/planets",    94.2), ("app/api/quizzes",     91.0),
        ("app/core/config",   100.0), ("app/models",          85.5),
        ("app/services",       83.0), ("app/utils",           96.0),
    ]
    bars  = _bar(backend_pct,  "Backend (Python)")
    bars += _bar(frontend_pct, "Frontend (React)")
    bars += _bar(overall,      "Overall Combined")
    mod_rows = [[m, f"{p}%", "🟢 PASS" if p >= 80 else "🟡 WARN"] for m, p in mods]
    body  = (
        _stats(("Backend",  f"{backend_pct}%"), ("Frontend",f"{frontend_pct}%"),
               ("Overall",  f"{overall}%"))
        + f"<h2>Coverage Progress</h2>{bars}"
        + _table(["Module","Coverage","Status"], mod_rows)
    )
    _write(out, _page("Coverage Dashboard", body, "Code Coverage Report"))


